  id INT NOT NULL AUTO_INCREMENT,
  device_id VARCHAR(100) NOT NULL COMMENT '设备编号',
  power_status ENUM('powered', 'unpowered') DEFAULT NULL COMMENT '带电状态（MQTT）',
  tag_status ENUM('tagged', 'untagged') DEFAULT NULL COMMENT '挂牌状态（MQTT）',
